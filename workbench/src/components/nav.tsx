export default function Nav() {
  const pathname = usePathname();
  const [theme, setThemeState] = useState<Theme>("light");

  useEffect(() => {
    initTheme();
    setThemeState(getTheme());
  }, []);

  const handleToggleTheme = () => {
    const newTheme = toggleTheme();
    setThemeState(newTheme);
  };

  return (
    <>
      <style jsx global>{`
        @media (orientation: portrait) {
          .nav-glass {
            backdrop-filter: blur(16px) saturate(180%);
            -webkit-backdrop-filter: blur(16px) saturate(180%);
            background: rgba(255, 255, 255, 0.7) !important;
            border-top: 1px solid rgba(255, 255, 255, 0.3) !important;
            border-right: none !important;
            box-shadow: 0 -4px 16px rgba(0, 0, 0, 0.1);
          }
          .dark .nav-glass {
            background: rgba(0, 0, 0, 0.5) !important;
            border-top: 1px solid rgba(255, 255, 255, 0.1) !important;
          }
          .nav-item-active {
            filter: drop-shadow(0 0 8px rgba(59, 130, 246, 0.6)) drop-shadow(0 0 16px rgba(59, 130, 246, 0.4));
          }
        }
      `}</style>
      <nav className="nav-glass w-48 shrink-0 border-r border-neutral-200 dark:border-neutral-800 bg-neutral-50 dark:bg-neutral-900 min-h-screen p-4 flex flex-col portrait:w-full portrait:h-auto portrait:min-h-0 portrait:fixed portrait:bottom-0 portrait:left-0 portrait:right-0 portrait:flex-row portrait:py-1 portrait:px-0 portrait:z-50">
        <div className="text-sm font-semibold text-neutral-400 dark:text-neutral-500 uppercase tracking-wider mb-3 px-2 portrait:hidden">
          Workbench
        </div>
        <div className="flex flex-col gap-1 flex-1 portrait:flex-row portrait:justify-evenly portrait:gap-0 portrait:flex-none portrait:w-full">
          {sections.map(({ href, label, icon }) => {
            const active =
              href === "/"
                ? pathname === "/"
                : pathname === href || pathname.startsWith(href + "/");
            return (
              <Link
                key={href}
                href={href}
                className={`block px-2 py-1.5 rounded text-sm transition-all duration-200 portrait:flex portrait:flex-col portrait:items-center portrait:gap-0.5 portrait:py-1.5 portrait:px-1 portrait:rounded-none portrait:flex-1 ${
                  active
                    ? "bg-neutral-200 dark:bg-neutral-800 text-neutral-900 dark:text-neutral-100 font-medium portrait:bg-transparent portrait:nav-item-active"
                    : "text-neutral-600 dark:text-neutral-400 hover:bg-neutral-100 dark:hover:bg-neutral-800/50 portrait:hover:bg-transparent"
                }`}
              >
                <span className="hidden portrait:block portrait:mb-0.5">{icon}</span>
                <span className="portrait:text-xs portrait:font-medium">{label}</span>
              </Link>
            );
          })}
        </div>
        <button
          onClick={handleToggleTheme}
          className="mt-4 px-2 py-1.5 rounded text-sm transition-colors text-neutral-600 dark:text-neutral-400 hover:bg-neutral-100 dark:hover:bg-neutral-800/50 text-left portrait:hidden"
          aria-label="Toggle theme"
        >
          {theme === "light" ? "🌙 Dark" : "☀️ Light"}
        </button>
      </nav>
    </>
  );
}
